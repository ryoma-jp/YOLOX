#!/usr/bin/env python3
# -*- coding:utf-8 -*-

import argparse
import json
import os
from typing import Dict, List, Tuple

import cv2
import numpy as np
from loguru import logger

EXPECTED_SCHEMA_VERSION = 4

DEFAULT_YOLOX_LAYER_MAPPING = {
    "backbone_s8": "backbone.backbone.dark3",
    "backbone_s16": "backbone.backbone.dark4",
    "backbone_s32": "backbone.backbone.dark5",
    "neck_s8": "backbone.C3_p3",
    "neck_s16": "backbone.C3_n3",
    "neck_s32": "backbone.C3_n4",
}


def make_parser():
    parser = argparse.ArgumentParser("Visualize YOLOX evaluation predictions")
    parser.add_argument(
        "--predictions-path",
        type=str,
        required=True,
        help="path to predictions.json generated by tools/eval.py --save-predictions",
    )
    parser.add_argument(
        "--images-dir",
        type=str,
        required=True,
        help="root directory that contains source images",
    )
    parser.add_argument(
        "--output-dir",
        type=str,
        required=True,
        help="directory to save visualization images",
    )
    parser.add_argument(
        "--image-id",
        type=int,
        required=True,
        help="target image_id. use -1 to render all image_id entries",
    )
    parser.add_argument(
        "--conf-threshold",
        type=float,
        default=0.0,
        help="confidence threshold for drawing boxes",
    )
    parser.add_argument(
        "--nms-iou-threshold",
        type=float,
        default=0.5,
        help="IoU threshold for per-class NMS. Set a negative value to disable NMS.",
    )
    parser.add_argument(
        "--annotations-path",
        type=str,
        default=None,
        help="path to COCO-format annotations JSON (e.g. instances_val2017.json). "
             "If provided, GT boxes are rendered as gt_{filename} alongside det_{filename}.",
    )
    parser.add_argument(
        "--mode",
        type=str,
        default="boxes",
        choices=["boxes", "features", "both"],
        help="kept for backward compatibility. feature npy export is always enabled.",
    )
    parser.add_argument(
        "--exp-file",
        type=str,
        default=None,
        help="path to experiment file used to build model for feature export",
    )
    parser.add_argument(
        "--ckpt",
        type=str,
        default=None,
        help="path to checkpoint (.pth) used to rerun inference for feature export",
    )
    parser.add_argument(
        "--device",
        type=str,
        default="cpu",
        help="device for rerun inference (e.g. cpu, cuda, cuda:0)",
    )
    parser.add_argument(
        "--feature-layers",
        nargs="*",
        default=None,
        help="optional logical layer names to export. default: all standard YOLOX feature layers",
    )
    return parser


def _load_annotations(annotations_path: str) -> Dict[str, List[dict]]:
    """Load a COCO-format annotations JSON and return a dict keyed by str(image_id)."""
    with open(annotations_path, "r", encoding="utf-8") as f:
        coco = json.load(f)

    if "annotations" not in coco:
        raise ValueError("Invalid annotations file: missing 'annotations' key")

    index: Dict[str, List[dict]] = {}
    for ann in coco["annotations"]:
        key = str(ann["image_id"])
        index.setdefault(key, []).append(ann)
    return index


def _load_predictions(predictions_path: str):
    with open(predictions_path, "r", encoding="utf-8") as f:
        payload = json.load(f)

    schema_version = payload.get("schema_version")
    if isinstance(schema_version, int) and schema_version < EXPECTED_SCHEMA_VERSION:
        logger.warning(
            "predictions schema_version={} is older than expected {}. "
            "Some fields may be missing.",
            schema_version,
            EXPECTED_SCHEMA_VERSION,
        )

    if "predictions" not in payload:
        raise ValueError("Invalid predictions file: missing 'predictions' key")

    predictions = payload["predictions"]
    images = payload.get("images", {})
    return payload, predictions, images


def _select_image_ids(predictions: Dict[str, dict], image_id: int) -> List[str]:
    if image_id == -1:
        return sorted(predictions.keys(), key=lambda x: int(x))

    key = str(image_id)
    if key not in predictions:
        raise KeyError("image_id={} was not found in predictions.json".format(image_id))
    return [key]


def _resolve_image_path(images_dir: str, image_id_key: str, pred: dict, images_meta: Dict[str, dict]):
    image_meta = images_meta.get(image_id_key, {}) if isinstance(images_meta, dict) else {}
    file_name = image_meta.get("file_name") or pred.get("file_name")
    if not file_name:
        raise ValueError(
            "image_id={} does not include file_name metadata. "
            "Regenerate predictions.json with current codebase.".format(image_id_key)
        )

    image_path = os.path.join(images_dir, file_name)
    return image_path, file_name


def _resolve_runtime_config(payload: dict, args) -> dict:
    metadata = payload.get("metadata", {}) if isinstance(payload, dict) else {}
    preprocess_meta = metadata.get("preprocess", {}) if isinstance(metadata, dict) else {}

    exp_file = args.exp_file or metadata.get("exp_file")
    ckpt = args.ckpt or metadata.get("checkpoint")
    test_size = metadata.get("test_size")
    model_family = metadata.get("model_family", "yolox")
    legacy = bool(preprocess_meta.get("legacy", False))

    if not exp_file:
        raise ValueError(
            "Unable to resolve exp file. Provide --exp-file or regenerate predictions.json with schema v4 metadata."
        )
    if not ckpt:
        raise ValueError(
            "Unable to resolve checkpoint path. Provide --ckpt or regenerate predictions.json with schema v4 metadata."
        )

    return {
        "model_family": str(model_family),
        "exp_file": str(exp_file),
        "ckpt": str(ckpt),
        "device": str(args.device),
        "test_size": test_size,
        "legacy": legacy,
    }


def _resolve_layer_mapping(model_family: str, selected_layers: List[str]) -> Dict[str, str]:
    if model_family != "yolox":
        raise NotImplementedError("model_family='{}' is not supported yet".format(model_family))

    base_mapping = dict(DEFAULT_YOLOX_LAYER_MAPPING)
    if not selected_layers:
        return base_mapping

    unknown = [name for name in selected_layers if name not in base_mapping]
    if unknown:
        raise KeyError("Unknown feature layer names: {}".format(", ".join(unknown)))

    return {name: base_mapping[name] for name in selected_layers}


def _load_model_for_feature_export(runtime_cfg: dict):
    import torch
    from yolox.data.data_augment import ValTransform
    from yolox.exp import get_exp

    exp = get_exp(runtime_cfg["exp_file"], None)
    model = exp.get_model()

    ckpt = torch.load(runtime_cfg["ckpt"], map_location="cpu")
    if isinstance(ckpt, dict) and "model" in ckpt:
        ckpt = ckpt["model"]
    model.load_state_dict(ckpt)

    test_size = runtime_cfg.get("test_size")
    if isinstance(test_size, list) and len(test_size) == 2:
        test_size = (int(test_size[0]), int(test_size[1]))
    else:
        test_size = tuple(exp.test_size)

    device = torch.device(runtime_cfg["device"])
    model.to(device)
    model.eval()

    preproc = ValTransform(legacy=runtime_cfg.get("legacy", False))
    return model, preproc, test_size, torch, device


def _register_feature_hooks(model, layer_mapping: Dict[str, str]):
    feature_cache = {}
    handles = []
    module_dict = dict(model.named_modules())

    missing = [module_name for module_name in layer_mapping.values() if module_name not in module_dict]
    if missing:
        raise KeyError(
            "Failed to find feature layers in model.named_modules(): {}".format(", ".join(missing))
        )

    for logical_name, module_name in layer_mapping.items():
        module = module_dict[module_name]

        def _hook(_, __, output, logical_name_=logical_name, module_name_=module_name):
            tensor = output[0] if isinstance(output, (list, tuple)) else output
            if tensor is None or not hasattr(tensor, "detach"):
                return
            feature_cache[logical_name_] = {
                "module_name": module_name_,
                "tensor": tensor.detach().cpu(),
            }

        handles.append(module.register_forward_hook(_hook))

    return handles, feature_cache


def _prepare_model_input(image: np.ndarray, preproc, test_size: Tuple[int, int], torch, device):
    processed, _ = preproc(image, None, test_size)
    model_input = torch.from_numpy(processed).unsqueeze(0).float().to(device)
    return model_input


def _save_feature_arrays(
    per_image_dir: str,
    image_id_key: str,
    file_name: str,
    feature_cache: Dict[str, dict],
    model_input_shape: Tuple[int, ...],
):
    npy_dir = os.path.join(per_image_dir, "npy")
    os.makedirs(npy_dir, exist_ok=True)

    manifest = {
        "image_id": image_id_key,
        "file_name": file_name,
        "model_input_shape": list(model_input_shape),
        "layers": [],
    }

    for logical_name in sorted(feature_cache.keys()):
        item = feature_cache[logical_name]
        tensor = item["tensor"]
        if tensor.ndim == 4 and tensor.shape[0] == 1:
            tensor = tensor[0]

        array = tensor.float().numpy()
        file_base = "feat_{}.npy".format(logical_name)
        file_path = os.path.join(npy_dir, file_base)
        np.save(file_path, array)

        manifest["layers"].append(
            {
                "logical_name": logical_name,
                "module_name": item["module_name"],
                "tensor_path": os.path.join("npy", file_base),
                "shape": list(array.shape),
                "dtype": str(array.dtype),
            }
        )

    manifest_path = os.path.join(per_image_dir, "feature_manifest.json")
    with open(manifest_path, "w", encoding="utf-8") as f:
        json.dump(manifest, f, ensure_ascii=False, indent=2)


def _category_color(category_id: int):
    # Deterministic color from category_id to avoid dependency on dataset class mapping.
    r = (37 * category_id + 17) % 255
    g = (17 * category_id + 59) % 255
    b = (29 * category_id + 101) % 255
    return int(b), int(g), int(r)


def _box_iou_xyxy(box_a: np.ndarray, box_b: np.ndarray) -> float:
    x0 = max(float(box_a[0]), float(box_b[0]))
    y0 = max(float(box_a[1]), float(box_b[1]))
    x1 = min(float(box_a[2]), float(box_b[2]))
    y1 = min(float(box_a[3]), float(box_b[3]))

    inter_w = max(0.0, x1 - x0)
    inter_h = max(0.0, y1 - y0)
    inter_area = inter_w * inter_h
    if inter_area <= 0.0:
        return 0.0

    area_a = max(0.0, float(box_a[2]) - float(box_a[0])) * max(0.0, float(box_a[3]) - float(box_a[1]))
    area_b = max(0.0, float(box_b[2]) - float(box_b[0])) * max(0.0, float(box_b[3]) - float(box_b[1]))
    union = area_a + area_b - inter_area
    if union <= 0.0:
        return 0.0
    return inter_area / union


def _nms_indices_per_class(boxes: np.ndarray, scores: np.ndarray, categories: np.ndarray, iou_threshold: float):
    if boxes.size == 0:
        return np.array([], dtype=np.int64)

    keep_indices = []
    unique_categories = np.unique(categories)

    for category_id in unique_categories:
        category_mask = categories == category_id
        category_indices = np.where(category_mask)[0]
        category_scores = scores[category_mask]
        order = category_indices[np.argsort(-category_scores)]

        while order.size > 0:
            current = int(order[0])
            keep_indices.append(current)

            if order.size == 1:
                break

            remaining = order[1:]
            ious = np.array([_box_iou_xyxy(boxes[current], boxes[idx]) for idx in remaining], dtype=np.float32)
            order = remaining[ious <= iou_threshold]

    return np.array(sorted(keep_indices), dtype=np.int64)


def _filter_predictions_for_draw(pred: dict, conf_threshold: float, nms_iou_threshold: float):
    boxes = np.asarray(pred.get("bboxes", []), dtype=np.float32)
    scores = np.asarray(pred.get("scores", []), dtype=np.float32)
    categories = np.asarray(pred.get("categories", []), dtype=np.int32)

    if boxes.ndim != 2 or boxes.shape[1] != 4:
        return []
    if scores.ndim != 1 or categories.ndim != 1:
        return []
    if not (len(boxes) == len(scores) == len(categories)):
        return []

    conf_mask = scores >= float(conf_threshold)
    boxes = boxes[conf_mask]
    scores = scores[conf_mask]
    categories = categories[conf_mask]
    if boxes.size == 0:
        return []

    if nms_iou_threshold >= 0.0:
        keep = _nms_indices_per_class(boxes, scores, categories, float(nms_iou_threshold))
        boxes = boxes[keep]
        scores = scores[keep]
        categories = categories[keep]

    return list(zip(boxes.tolist(), scores.tolist(), categories.tolist()))


def _draw_gt_boxes(image: np.ndarray, gt_entries: List[dict]) -> np.ndarray:
    """Draw Ground-Truth boxes (COCO xywh format) onto image."""
    for ann in gt_entries:
        bbox = ann.get("bbox")
        category_id = ann.get("category_id")
        if not bbox or len(bbox) != 4 or category_id is None:
            continue

        # COCO bbox is [x, y, w, h] → convert to xyxy
        x0 = int(bbox[0])
        y0 = int(bbox[1])
        x1 = int(bbox[0] + bbox[2])
        y1 = int(bbox[1] + bbox[3])

        color = _category_color(int(category_id))
        label = "gt_id{}".format(int(category_id))

        cv2.rectangle(image, (x0, y0), (x1, y1), color, 3)
        (tw, th), _ = cv2.getTextSize(label, cv2.FONT_HERSHEY_SIMPLEX, 0.45, 1)
        text_y0 = max(0, y0 - th - 4)
        cv2.rectangle(image, (x0, text_y0), (x0 + tw + 4, text_y0 + th + 4), color, -1)
        cv2.putText(
            image,
            label,
            (x0 + 2, text_y0 + th + 1),
            cv2.FONT_HERSHEY_SIMPLEX,
            0.45,
            (255, 255, 255),
            1,
            cv2.LINE_AA,
        )

    return image


def _draw_boxes(image: np.ndarray, pred: dict, conf_threshold: float, nms_iou_threshold: float):
    filtered_predictions = _filter_predictions_for_draw(pred, conf_threshold, nms_iou_threshold)

    for box, score, category_id in filtered_predictions:
        if len(box) != 4:
            continue

        x0, y0, x1, y1 = [int(v) for v in box]
        color = _category_color(int(category_id))
        label = "id{}:{:.1f}%".format(int(category_id), float(score) * 100.0)

        cv2.rectangle(image, (x0, y0), (x1, y1), color, 2)
        (tw, th), _ = cv2.getTextSize(label, cv2.FONT_HERSHEY_SIMPLEX, 0.45, 1)
        text_y0 = max(0, y0 - th - 4)
        cv2.rectangle(image, (x0, text_y0), (x0 + tw + 4, text_y0 + th + 4), color, -1)
        cv2.putText(
            image,
            label,
            (x0 + 2, text_y0 + th + 1),
            cv2.FONT_HERSHEY_SIMPLEX,
            0.45,
            (255, 255, 255),
            1,
            cv2.LINE_AA,
        )

    return image


def _render_boxes_mode(
    predictions: Dict[str, dict],
    images_meta: Dict[str, dict],
    image_ids: List[str],
    gt_index: Dict[str, List[dict]],
    model,
    preproc,
    test_size: Tuple[int, int],
    torch,
    device,
    layer_mapping: Dict[str, str],
    args,
):
    rendered_count = 0

    for image_id_key in image_ids:
        pred = predictions.get(image_id_key, {})
        if not isinstance(pred, dict):
            logger.warning("Skip image_id={} due to unsupported prediction payload.", image_id_key)
            continue

        image_path, file_name = _resolve_image_path(args.images_dir, image_id_key, pred, images_meta)
        image = cv2.imread(image_path)
        if image is None:
            logger.warning("Skip image_id={} because source image was not found: {}", image_id_key, image_path)
            continue

        safe_file_name = os.path.basename(file_name)
        per_image_dir = os.path.join(args.output_dir, image_id_key)
        os.makedirs(per_image_dir, exist_ok=True)

        # Prediction image: det_{filename}
        det_image = _draw_boxes(image.copy(), pred, args.conf_threshold, args.nms_iou_threshold)
        det_path = os.path.join(per_image_dir, "det_{}".format(safe_file_name))
        cv2.imwrite(det_path, det_image)

        # GT image: gt_{filename} (only when annotations were provided)
        if gt_index is not None:
            gt_entries = gt_index.get(image_id_key, [])
            gt_image = _draw_gt_boxes(image.copy(), gt_entries)
            gt_path = os.path.join(per_image_dir, "gt_{}".format(safe_file_name))
            cv2.imwrite(gt_path, gt_image)

        hook_handles, feature_cache = _register_feature_hooks(model, layer_mapping)
        model_input = None
        try:
            model_input = _prepare_model_input(image, preproc, test_size, torch, device)
            with torch.no_grad():
                _ = model(model_input)
        finally:
            for handle in hook_handles:
                handle.remove()

        if model_input is not None:
            _save_feature_arrays(
                per_image_dir=per_image_dir,
                image_id_key=image_id_key,
                file_name=safe_file_name,
                feature_cache=feature_cache,
                model_input_shape=tuple(model_input.shape),
            )

            input_array = model_input.detach().cpu().float().numpy()
            if input_array.ndim == 4 and input_array.shape[0] == 1:
                input_array = input_array[0]
            np.save(os.path.join(per_image_dir, "input_tensor.npy"), input_array)

        rendered_count += 1

    logger.info("Rendered {} images with detection overlays and feature npy into {}", rendered_count, args.output_dir)


def main():
    args = make_parser().parse_args()
    payload, predictions, images_meta = _load_predictions(args.predictions_path)

    logger.info("Loaded predictions schema_version={}", payload.get("schema_version", "unknown"))

    image_ids = _select_image_ids(predictions, args.image_id)

    gt_index = None
    if args.annotations_path is not None:
        gt_index = _load_annotations(args.annotations_path)
        logger.info("Loaded GT annotations from {}", args.annotations_path)

    runtime_cfg = _resolve_runtime_config(payload, args)
    layer_mapping = _resolve_layer_mapping(runtime_cfg["model_family"], args.feature_layers)
    model, preproc, test_size, torch, device = _load_model_for_feature_export(runtime_cfg)

    if args.mode != "boxes":
        logger.info("mode='{}' requested. Detection overlays and feature npy export are both enabled.", args.mode)

    _render_boxes_mode(
        predictions=predictions,
        images_meta=images_meta,
        image_ids=image_ids,
        gt_index=gt_index,
        model=model,
        preproc=preproc,
        test_size=test_size,
        torch=torch,
        device=device,
        layer_mapping=layer_mapping,
        args=args,
    )


if __name__ == "__main__":
    main()
